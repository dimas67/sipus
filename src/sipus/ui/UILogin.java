    private void bFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bFileActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Oracle Database", "dmp");
        chooser.setFileFilter(filter);
        if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            tFile.setText(chooser.getSelectedFile().toString());
        }
    }//GEN-LAST:event_bFileActionPerformed

    private void bRestoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRestoreActionPerformed
        // TODO add your handling code here:
        username = tUsername.getText();
        password = String.valueOf(tPassword.getPassword(), 0, tPassword.getPassword().length);
        if (tFile.getText().length() == 0) {
            OptionPane.showWarningMessage("Silakan pilih file penyimpanan");
        } else {
            if (isConnectionSuccess(username, password)) {
                setCursor(new Cursor(Cursor.WAIT_CURSOR));
                Restore restore = new Restore(tFile.getText());
                restore.setFromuser("dimas");
                restore.setUsername(username);
                restore.setPassword(password);
                restore.startRestore();
                setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
            }
        }
    }//GEN-LAST:event_bRestoreActionPerformed

