    private void login() {
        setCursor(new Cursor(Cursor.WAIT_CURSOR));
        if (adminDao == null) {
            adminDao = DaoService.getAdminDao();
        }
        String usernameL = tUser.getText();
        String passwordL = String.valueOf(tPass.getPassword(), 0, tPass.getPassword().length);
        Admin admin = adminDao.getAdmin(usernameL, passwordL);
        if (admin == null) {
            OptionPane.showWarningMessage("Login Gagal");
            clearForm();
        } else {
            if (admin.getStatus() == 1) {
                OptionPane.showWarningMessage("Anda sudah login");
            } else {
                dispose();
                admin.setId(admin.getId());
                admin.setStatus(1);
                adminDao.updateStatus(admin);
                mainForm.setAdmin(admin.getId(), admin.getUsername());
                mainForm.setConnectionText(DaoService.getConnection().toString());
                clearForm();
            }
        }
        setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
    }

    private void clearForm() {
        tUser.setText("");
        tPass.setText("");
        tUser.requestFocus();
    }

    public void setMainForm(MainForm mainForm) {
        this.mainForm = mainForm;
    }

    private boolean isConnectionSuccess(String username, String password) {
        boolean conn = false;
        try {
            Class.forName("oracle.jdbc.driver.OracleDriver");
            OracleDataSource oracleDataSource = new OracleDataSource();
            oracleDataSource.setURL("jdbc:oracle:thin:@localhost:1521:XE");
            oracleDataSource.setUser(username);
            oracleDataSource.setPassword(password);
            Connection connection = oracleDataSource.getConnection();
            conn = true;
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UIDatabase.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            conn = false;
            OptionPane.showErrorMessage("Koneksi Gagal : \n" + ex);
            Logger.getLogger(UIDatabase.class.getName()).log(Level.SEVERE, null, ex);
        }
        return conn;
    }

    private void simpan() {
        try {
            Properties properties = new Properties();
            properties.setProperty("Server", tServerC.getText());
            properties.setProperty("Port", tPortC.getText());
            properties.setProperty("Username", tUsernameC.getText());
            properties.setProperty("Password", String.valueOf(tPasswordC.getPassword(), 0, tPasswordC.getPassword().length));
            properties.store(new FileOutputStream("config/database.ini"), "Database Configuration");
            OptionPane.showMessage("Pengaturan database berhasil disimpan");
        } catch (IOException ex) {
            OptionPane.showErrorMessage(ex);
            Logger.getLogger(UIDatabase.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
